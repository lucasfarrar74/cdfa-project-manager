// Helper to get procedure by activity type and event type
export function getProcedureForActivity(
  activityType: 'trade_assistance' | 'educational',
  eventType?: string
): ProcedureTemplate | undefined {
  return defaultProcedures.find(
    (p) => p.activityType === activityType && (!eventType || p.eventType === eventType)
  );
}
